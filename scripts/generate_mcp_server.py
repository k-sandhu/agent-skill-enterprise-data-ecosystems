def build_table_records(tables: list[TableSpec]) -> list[dict[str, Any]]:
    """Manifest table entries, sorted by logical key. Naming and FK extraction
    come straight from TableSpec so they match the built database exactly."""
    records = []
    for tbl in tables:
        fk_by_col = {col: ref for col, ref in tbl.fk_refs()}
        columns = []
        for col in tbl.columns:
            columns.append({
                "name": col.get("name"),
                "type": col.get("type", "string"),
                "nullable": col.get("nullable", True) is not False,
                "sensitivity": col.get("sensitivity"),
                "fk_target": fk_by_col.get(col.get("name")),
            })
        records.append({
            "key": tbl.key,
            "physical": tbl.physical,
            "layer": tbl.layer,
            "source": tbl.source,
            "source_system": tbl.source_system,
            "purpose": tbl.purpose,
            "grain": tbl.grain,
            "primary_key": list(tbl.primary_key),
            "natural_key": list(tbl.natural_key),
            "traits": list(tbl.raw.get("traits", [])),
            "columns": columns,
        })
    records.sort(key=lambda record: record["key"])
    return records


def _token_to_key(tables: list[TableSpec]) -> dict[str, str]:
    """Map every recognizable table token (logical key and physical name) to its
    canonical logical key."""
    mapping: dict[str, str] = {}
    for tbl in tables:
        mapping[tbl.key] = tbl.key
        mapping[tbl.key.lower()] = tbl.key
        mapping[tbl.physical] = tbl.key
        mapping[tbl.physical.lower()] = tbl.key
    return mapping


def _token_regex(tables: list[TableSpec]) -> "re.Pattern[str]":
    """Word-boundary alternation over every known table token, longest first so
    physical names never partially shadow logical keys."""
    tokens = set()
    for tbl in tables:
        tokens.add(tbl.key)
        tokens.add(tbl.physical)
    ordered = sorted(tokens, key=len, reverse=True)
    if not ordered:
        return re.compile(r"(?!x)x")  # matches nothing
    alternation = "|".join(re.escape(token) for token in ordered)
    return re.compile(r"\b(" + alternation + r")\b", re.IGNORECASE)


_WRITE_TARGET_PATTERNS = [
    re.compile(r"insert\s+(?:or\s+\w+\s+)?into\s+[\"'\[]?([A-Za-z_][\w.]*)", re.IGNORECASE),
    re.compile(r"create\s+(?:temp(?:orary)?\s+)?(?:view|table)\s+(?:if\s+not\s+exists\s+)?"
               r"[\"'\[]?([A-Za-z_][\w.]*)", re.IGNORECASE),
    re.compile(r"replace\s+into\s+[\"'\[]?([A-Za-z_][\w.]*)", re.IGNORECASE),
    re.compile(r"update\s+[\"'\[]?([A-Za-z_][\w.]*)", re.IGNORECASE),
]


def _derivation_sql(deriv: dict[str, Any]) -> str:
    sql = deriv.get("sql", "")
    if isinstance(sql, list):
        sql = "\n".join(str(part) for part in sql)
    return str(sql)


def extract_lineage(spec: dict[str, Any], tables: list[TableSpec]) -> list[dict[str, Any]]:
    """Build lineage edges from spec derivations (write target downstream, every
    referenced table upstream) plus dataflows (consecutive recognized tables in
    the declared sequence). Over-approximates slightly on pathological names by
    design; sorted and de-duplicated for determinism."""
    token_to_key = _token_to_key(tables)
    token_regex = _token_regex(tables)
    edges: set[tuple[str, str, str, str]] = set()

    def resolve(token: str) -> str | None:
        return token_to_key.get(token) or token_to_key.get(token.lower())

    # --- derivations ---
    for index, deriv in enumerate(spec.get("derivations", [])):
        name = deriv.get("name", f"derivation_{index}")
        sql = _derivation_sql(deriv)
        referenced = {resolve(m.group(1)) for m in token_regex.finditer(sql)}
        referenced.discard(None)
        downstream = set()
        for pattern in _WRITE_TARGET_PATTERNS:
            for match in pattern.finditer(sql):
                key = resolve(match.group(1))
                if key is not None:
                    downstream.add(key)
        upstream = referenced - downstream
        for target in downstream:
            for source in upstream:
                if source != target:
                    edges.add((source, target, name, "derivation"))

    # --- dataflows ---
    for index, flow in enumerate(spec.get("dataflows", [])):
        name = flow.get("name", f"dataflow_{index}")
        sequence = flow.get("sequence") or flow.get("path") or ""
        chain: list[str] = []
        for match in token_regex.finditer(str(sequence)):
            key = resolve(match.group(1))
            if key is not None and (not chain or chain[-1] != key):
                chain.append(key)
        # Explicit source/target fields, if a spec ever declares them.
        for field_from, field_to in (("source", "target"), ("from", "to")):
            src = flow.get(field_from)
            dst = flow.get(field_to)
            if src and dst:
                src_key = resolve(str(src))
                dst_key = resolve(str(dst))
                if src_key and dst_key and src_key != dst_key:
                    edges.add((src_key, dst_key, name, "dataflow"))
        for first, second in zip(chain, chain[1:]):
            if first != second:
                edges.add((first, second, name, "dataflow"))

    ordered = sorted(edges, key=lambda edge: (edge[0], edge[1], edge[2]))
    return [{"from": f, "to": t, "via": v, "kind": k} for (f, t, v, k) in ordered]


def build_resources(build_dir: Path, mcp_dir: Path) -> list[dict[str, Any]]:
    """List documentation resources that exist on disk at generation time,
    sorted by uri. Paths are stored relative to the manifest directory."""
    resources = []
    for uri, filename, mimetype, description in DOC_RESOURCES:
        target = build_dir / filename
        if target.exists():
            rel = Path(os.path.relpath(target, mcp_dir)).as_posix()
            resources.append({"uri": uri, "path": rel, "mimeType": mimetype,
                              "description": description})
    # DDL files under <build>/sqlite/.
    sql_dir = build_dir / "sqlite"
    if sql_dir.is_dir():
        for sql_file in sorted(sql_dir.glob("*.sql")):
            basename = sql_file.stem
            rel = Path(os.path.relpath(sql_file, mcp_dir)).as_posix()
            resources.append({"uri": f"ecosystem://ddl/{basename}", "path": rel,
                              "mimeType": "application/sql",
                              "description": f"DDL artifact {sql_file.name}."})
    # The generated README always ships in the mcp/ directory itself.
    resources.append({"uri": "ecosystem://docs/server-readme", "path": "README.md",
                      "mimeType": "text/markdown",
                      "description": "This server's connection guide."})
    resources.sort(key=lambda resource: resource["uri"])
    return resources


def build_manifest(spec: dict[str, Any], tables: list[TableSpec], server_name: str,
                   db_rel: str, resources: list[dict[str, Any]]) -> dict[str, Any]:
    org = spec.get("organization", {})
    time_block = spec.get("time", {})
    return {
        "manifest_version": 1,
        "server_name": server_name,
        "engine_version": ENGINE_VERSION,
        "database": db_rel,
        "disclaimer": DISCLAIMER,
        "organization": {
            "name": org.get("name"),
            "archetype": org.get("archetype"),
            "industry": org.get("industry"),
            "currency": org.get("currency"),
        },
        "time": {
            "start_date": time_block.get("start_date"),
            "end_date": time_block.get("end_date"),
            "as_of_date": time_block.get("as_of_date") or time_block.get("end_date"),
        },
        "tables": build_table_records(tables),
        "lineage": extract_lineage(spec, tables),
        "controls": spec.get("controls", []),
        "dq_rules": spec.get("dq_rules", []),
        "required_views": (spec.get("validation", {}) or {}).get("required_views", []),
        "resources": resources,
        "limits": dict(LIMITS),
    }


