#!/usr/bin/env python3
"""Generate a read-only MCP server package for a built ecosystem.

Emits three files into ``<build>/mcp/``:

  * ``server.py``          -- a byte-for-byte copy of scripts/mcp_server_template.py
  * ``mcp_manifest.json``  -- ecosystem-specific knowledge (tables, lineage,
                              controls, DQ rules, resources, limits)
  * ``README.md``          -- a connection guide for Claude Code / Desktop / any
                              stdio MCP host

It also writes a clean, deterministic copy of the spec to
``<build>/ecosystem_spec.json`` so the ``ecosystem://spec`` resource travels with
the package (relative path, relocatable, no absolute paths in the manifest).

The server template and manifest are stdlib-only and never touch the network.
Naming (logical -> physical), layer parsing, and FK extraction reuse
``build_sqlite_ecosystem`` so they are identical to what the engine built.

Determinism is a contract: the three emitted files are byte-identical across
runs, machines, and PYTHONHASHSEED values (no timestamps, sorted JSON keys,
explicit ``\\n`` newlines, only package-relative paths).

Usage:
  python scripts/generate_mcp_server.py <spec.json> --build <dir> \\
      [--db <path>] [--name <server-name>] [--force] [--quiet]

Exit codes: 0 success, 1 generation failure, 2 usage error.
"""

