# Database resolution
# ---------------------------------------------------------------------------


def resolve_database(spec: dict[str, Any], build_dir: Path, db_arg: Path | None) -> Path:
    if db_arg is not None:
        db_path = db_arg
    else:
        org_name = spec.get("organization", {}).get("name", "ecosystem")
        default_db = build_dir / (physical_name(org_name) + ".db")
        if default_db.exists():
            db_path = default_db
        else:
            db_files = sorted(build_dir.glob("*.db"))
            if len(db_files) == 1:
                db_path = db_files[0]
            elif not db_files:
                raise SpecError(f"no .db file found in {build_dir}; build the ecosystem first "
                                "or pass --db.")
            else:
                raise SpecError(f"multiple .db files in {build_dir}: "
                                f"{[f.name for f in db_files]}; disambiguate with --db.")
    if not db_path.exists():
        raise SpecError(f"database not found: {db_path}")
    try:
        conn = sqlite3.connect(f"file:{db_path.as_posix()}?mode=ro", uri=True)
        conn.execute("select 1")
        conn.close()
    except sqlite3.Error as exc:
        raise SpecError(f"cannot open database read-only: {db_path}: {exc}")
    return db_path


# ---------------------------------------------------------------------------
# Orchestration
# ---------------------------------------------------------------------------


def generate(spec_path: Path, build_dir: Path, db_arg: Path | None, name_arg: str | None,
             force: bool, quiet: bool) -> Path:
    spec = load_spec(spec_path)
    org_name = spec.get("organization", {}).get("name", "ecosystem")
    tables = [TableSpec(raw, i, spec) for i, raw in enumerate(spec.get("tables", []))]
    if not tables:
        raise SpecError("spec has no tables.")

    db_path = resolve_database(spec, build_dir, db_arg)
    server_name = name_arg or (physical_name(org_name).replace("_", "-") + "-data-ecosystem")

    mcp_dir = build_dir / "mcp"
    emitted = [mcp_dir / "server.py", mcp_dir / "mcp_manifest.json", mcp_dir / "README.md"]
    if mcp_dir.exists() and any(path.exists() for path in emitted) and not force:
        raise SpecError(f"{mcp_dir} already contains a generated server. Pass --force to "
                        "overwrite (regeneration is the expected workflow after a rebuild).")
    mcp_dir.mkdir(parents=True, exist_ok=True)

    # Write a clean, deterministic spec copy into the build directory so the
    # ecosystem://spec resource is BOM-free, note-stripped, and relocatable.
    spec_copy = build_dir / "ecosystem_spec.json"
    if not (spec_copy.exists() and spec_copy.resolve() == spec_path.resolve()):
        with spec_copy.open("w", encoding="utf-8", newline="\n") as handle:
            handle.write(json.dumps(spec, indent=2, sort_keys=True))
            handle.write("\n")

    db_rel = Path(os.path.relpath(db_path, mcp_dir)).as_posix()
    resources = build_resources(build_dir, mcp_dir)
    manifest = build_manifest(spec, tables, server_name, db_rel, resources)

    # server.py: byte-for-byte copy of the template.
    template_bytes = TEMPLATE.read_bytes()
    (mcp_dir / "server.py").write_bytes(template_bytes)

    # mcp_manifest.json: sorted keys, explicit newline.
    with (mcp_dir / "mcp_manifest.json").open("w", encoding="utf-8", newline="\n") as handle:
        handle.write(json.dumps(manifest, indent=2, sort_keys=True))
        handle.write("\n")

    # README.md.
    with (mcp_dir / "README.md").open("w", encoding="utf-8", newline="\n") as handle:
        handle.write(render_readme(server_name, manifest))

    log(f"MCP server package written to {mcp_dir}", quiet)
    log(f"  server.py        ({len(template_bytes):,} bytes, copied verbatim)", quiet)
    log(f"  mcp_manifest.json ({len(manifest['tables'])} tables, "
        f"{len(manifest['lineage'])} lineage edges, {len(resources)} resources)", quiet)
    log(f"  README.md", quiet)
    log(f"Connect with: claude mcp add {server_name} -- python "
        f"{(mcp_dir / 'server.py')}", quiet)
    return mcp_dir


def main(argv: list[str] | None = None) -> int:
    for stream in (sys.stdout, sys.stderr):
        if hasattr(stream, "reconfigure"):
            stream.reconfigure(encoding="utf-8", errors="replace")
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("spec", type=Path, help="Path to the ecosystem spec JSON.")
    parser.add_argument("--build", type=Path, required=True,
                        help="Build output directory (emits <build>/mcp/).")
    parser.add_argument("--db", type=Path, default=None,
                        help="Database path (default: <build>/<org>.db or the sole *.db).")
    parser.add_argument("--name", default=None, help="Server name (default derived from the org).")
    parser.add_argument("--force", action="store_true", help="Overwrite an existing mcp/ package.")
    parser.add_argument("--quiet", action="store_true", help="Suppress progress output.")
    args = parser.parse_args(argv)

    if not args.spec.exists():
        print(f"error: spec file not found: {args.spec}", file=sys.stderr)
        return 2
    if not args.build.exists():
        print(f"error: build directory not found: {args.build}", file=sys.stderr)
        return 2
    if not TEMPLATE.exists():
        print(f"error: server template missing: {TEMPLATE}", file=sys.stderr)
        return 2
    try:
        generate(args.spec, args.build, args.db, args.name, args.force, args.quiet)
        return 0
    except SpecError as exc:
        print(f"GENERATION ERROR: {exc}", file=sys.stderr)
        return 1
    except sqlite3.Error as exc:
        print(f"DATABASE ERROR: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())
